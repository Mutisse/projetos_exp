    private void btn_regitrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_regitrarActionPerformed
        try {
            Cliente add = new Cliente();
            if (txtNome.getText().length() > 1) {
                if (txtApelido.getText().length() > 1) {
                    if ((Float.parseFloat(spnValorDivida.getValue().toString()) != 0)
                            && (Float.parseFloat(spnValorDivida.getValue().toString()) <= 5000)
                            && (Float.parseFloat(lblValorApagar.getText()) != 0)) {

                        add.setId(lbl_id.getText());
                        add.setNomeDevedor(txtNome.getText());
                        add.setApelidoDevedor(txtApelido.getText());
                        add.setContacto(txf_contacto.getText());
                        add.setData(jDateChooser.getDate());
                        if (rb_Masculino.isSelected()) {
                            add.setGenero(rb_Masculino.getActionCommand());
                        } else if (rb_Femenino.isSelected()) {
                            add.setGenero(rb_Femenino.getActionCommand());
                        }
                        add.setValorDivida(Float.parseFloat(spnValorDivida.getValue().toString()));
                        add.setValorAPagar(Float.parseFloat(lblValorApagar.getText()));
                        add.setRemanescente(add.getValorAPagar());
                        add.setEstadoDivida(false);

                        ClienteController.create(add);
                        JOptionPane.showMessageDialog(rootPane, "Registado com sucesso!");
                        LimpatCampos();
                        setarId();
                        preencherTabela();
                        jTabbedPane.setSelectedIndex(1);

                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Valor de empréstimo inválido!");
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Apelido inválido!");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Nome inválido!");
            }

        } catch (NumberFormatException ex) {

        } catch (NullPointerException ex) {

        }
    }//GEN-LAST:event_btn_regitrarActionPerformed



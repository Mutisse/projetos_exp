    public void FormatDatachoos() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MMMM/yyyy");
        String data = sdf.format(Calendar.getInstance().getTime());
        try {
            jDateChooser.setDate(sdf.parse(data));
            jDateChooser.setMaxSelectableDate(sdf.parse(data));

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro na conversão da data!");
        }
    }

    private void calcularValor() {
        float valor2, valorDivida = Float.parseFloat(spnValorDivida.getValue().toString());

        if (valorDivida <= 100.00) {
            valor2 = (float) (valorDivida + valorDivida * 0.4);
            lblValorApagar.setText(" " + Float.toString(valor2));
        } else if (valorDivida <= 500.00) {
            valor2 = (float) (valorDivida + valorDivida * 0.3);
            lblValorApagar.setText(" " + Float.toString(valor2));
        } else if (valorDivida <= 1000.00) {
            valor2 = (float) (valorDivida + valorDivida * 0.2);
            lblValorApagar.setText(" " + Float.toString(valor2));
        } else {
            valor2 = (float) (valorDivida + valorDivida * 0.15);
            lblValorApagar.setText(" " + Float.toString(valor2));
        }
    }

    private void setarId() {
        ClienteController controller = new ClienteController();
        controller.CaminhoPrincipal();

        Calendar now = Calendar.getInstance();

        String id = "";
        boolean existe = false;
        if (!ClienteController.lista().isEmpty()) {

            while (true) {

                id = ((int) (10000 * Math.random())) + " ";
                for (Cliente divida : ClienteController.lista()) {
                    if (id == null ? divida.getId() == null : id.equals(divida.getId())) {
                        existe = true;

                        break;
                    }
                }
                if (existe == false) {
                    break;
                }
            }
        } else {
            id = ((int) (10000 * Math.random())) + " ";
        }

        lbl_id.setText(" " + id);
    }

    private void preencherTabela() {

        Cliente[] array = new Cliente[ClienteController.lista().size()];
        int i = 0;
        for (Cliente d : ClienteController.lista()) {
            array[i] = d;
            i++;
        }

        Arrays.sort(array);   //FAZ A ORDENAÇÃO DO ARRAY "DÍVIDAS" EM ORDEM CRESCENTE DOS VALORES A PAGAR
        model.setNumRows(0);
        for (Cliente dadosCliente : array) {
            dataf = sdf.format(dadosCliente.getData());
            model.addRow(new Object[]{dadosCliente.getId(), dadosCliente.getNomeDevedor() + " " + dadosCliente.getApelidoDevedor(), dadosCliente.getGenero(), dadosCliente.getContacto(), dataf,
                dadosCliente.getValorDivida(), dadosCliente.getValorAPagar(), dadosCliente.getRemanescente(), dadosCliente.getEstadoDivida()});

        }
    }


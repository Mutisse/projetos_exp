    public void setar_campos() {
        for (Usuarios user : UsuarioController.lista()) {
            txtNome.setText(user.getNome());
            txtUser.setText(user.getUsuario());
            if (user.getGenero().equals("Masculino")) {
                rb_Masculino.setSelected(true);
            } else if (user.getGenero().equals("Feminino")) {
                rb_Femenino.setSelected(true);
            }
            pfSenha.setText(user.getSenha());
            pfSenha1.setText(user.getSenha());
        }

    }


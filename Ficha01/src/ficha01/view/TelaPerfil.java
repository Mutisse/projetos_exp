    private void btnRegistar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistar1ActionPerformed
        Usuarios add = new Usuarios();
        add.setNome(txtNome.getText());
        add.setUsuario(txtUser.getText());
        if (rb_Masculino.isSelected()) {
            add.setGenero(rb_Masculino.getActionCommand());
        } else if (rb_Femenino.isSelected()) {
            add.setGenero(rb_Femenino.getActionCommand());
        }
        add.setSenha(pfSenha.getText());

        try {
            if (txtNome.getText().length() > 1) {
                if (txtUser.getText().length() > 1) {
                    if (pfSenha.getText().length() > 3 || pfSenha1.getText().length() > 3) {
                        if (pfSenha.getText().equals(pfSenha1.getText())) {
                            UsuarioController.create(add);
                            JOptionPane.showMessageDialog(null, "Registado com sucesso!", "Notificacao", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(null, "Senha inválida.\n a primeira senha nao e igual a Segunda !", "Notificacao", JOptionPane.INFORMATION_MESSAGE);
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Senha inválida.\nCombine pelo menos 4 caracteres!", "Notificacao", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {

                }
            } else {

            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro de formatação. Verifique os seus dados!", "", JOptionPane.ERROR_MESSAGE);
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos!", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistar1ActionPerformed

    private void jCsenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCsenhaActionPerformed
        if (jCsenha.isSelected()) {
            pfSenha1.setEchoChar((char) 0);
            pfSenha.setEchoChar((char) 0);
        } else {
            pfSenha.setEchoChar('*');
            pfSenha1.setEchoChar('*');

        }
    }//GEN-LAST:event_jCsenhaActionPerformed
